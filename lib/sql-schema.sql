-- ----------------------------------------------------------------------------
-- 2. NOTIFICATIONS TABLE (System logs & Alerts)
-- ----------------------------------------------------------------------------
create table if not exists public.notifications (
  id bigint generated by default as identity primary key,
  message text not null,
  type text not null,
  created_at timestamptz not null default now(),
  display_timestamp text
);

-- ----------------------------------------------------------------------------
-- 3. USER PROFILES TABLE (Authentication & Metadata)
-- ----------------------------------------------------------------------------
create table if not exists public.user_profiles (
  id uuid primary key default gen_random_uuid(),
  email text unique not null,
  password_hash text not null,
  full_name text,
  role text, -- e.g., 'farmer', 'officer', 'admin', 'fisherfolk'
  phone text,
  address text,
  municipality text,
  barangay text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- ----------------------------------------------------------------------------
-- 4. EVENTS TABLE (Department events & Activities)
-- ----------------------------------------------------------------------------
create table if not exists public.events (
  id bigint generated by default as identity primary key,
  title text not null,
  date text not null, -- formatted text (e.g. 'February 10, 2026') or ISO string
  time text not null, -- formatted time range (e.g. '9:00 AM – 3:00 PM')
  venue text not null,
  audience text not null,
  description text not null,
  status text not null default 'upcoming',
  created_at timestamptz not null default now()
);

-- ----------------------------------------------------------------------------
-- 5. ATTENDANCE TABLE (Tracks event attendance via QR scans)
-- ----------------------------------------------------------------------------
create table if not exists public.attendance (
  id bigint generated by default as identity primary key,
  event_id bigint not null references public.events(id) on delete cascade,
  record_id bigint not null references public.records(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (event_id, record_id)
);

-- ----------------------------------------------------------------------------
-- 6. VIEWS (Analytics & Summary Metrics)
-- ----------------------------------------------------------------------------
create or replace view public.analytics_overview as
select
  count(*)::integer as total_registered,
  count(*) filter (where lower(coalesce(farmer_fisherfolk_both, type, '')) = 'farmer')::integer as total_farmers,
  count(*) filter (where lower(coalesce(farmer_fisherfolk_both, type, '')) = 'fisherfolk')::integer as total_fisherfolks,
  count(*) filter (where lower(coalesce(status, '')) = 'active')::integer as active_members,
  count(*) filter (where lower(coalesce(status, '')) = 'inactive')::integer as inactive_members,
  case
    when count(*) = 0 then 0
    else round((count(*) filter (where lower(coalesce(farmer_fisherfolk_both, type, '')) = 'farmer')::numeric / count(*)::numeric) * 100, 1)
  end as percentage_farmers,
  case
    when count(*) = 0 then 0
    else round((count(*) filter (where lower(coalesce(farmer_fisherfolk_both, type, '')) = 'fisherfolk')::numeric / count(*)::numeric) * 100, 1)
  end as percentage_fisherfolks,
  case
    when count(*) = 0 then 0
    else round((count(*) filter (where lower(coalesce(status, '')) = 'active')::numeric / count(*)::numeric) * 100, 1)
  end as percentage_active,
  case
    when count(*) = 0 then 0
    else round((count(*) filter (where lower(coalesce(status, '')) = 'inactive')::numeric / count(*)::numeric) * 100, 1)
  end as percentage_inactive
from public.records;

