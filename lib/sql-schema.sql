-- Password verification function for user_profiles
create or replace function public.verify_password(password_input text, password_hash text)
returns boolean as $$
begin
  return password_hash = crypt(password_input, password_hash);
end;
$$ language plpgsql;

-- Trigger function to update updated_at timestamp automatically
create or replace function public.update_updated_at_column()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

-- Bind updated_at trigger to user_profiles
drop trigger if exists update_user_profiles_updated_at on public.user_profiles;
create trigger update_user_profiles_updated_at
before update on public.user_profiles
for each row
execute function public.update_updated_at_column();

